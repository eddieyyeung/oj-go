package solution

func uniquePaths(m int, n int) int {
	dp := [100][100]int{}
	for i := 0; i < m; i++ {
		dp[i][0] = 1
	}
	for j := 0; j < n; j++ {
		dp[0][j] = 1
	}
	for i := 1; i < m; i++ {
		for j := 1; j < n; j++ {
			dp[i][j] = dp[i-1][j] + dp[i][j-1]
		}
	}
	return dp[m-1][n-1]
}

// Run ...
func Run(m int, n int) int {
	return uniquePaths(m, n)
}
